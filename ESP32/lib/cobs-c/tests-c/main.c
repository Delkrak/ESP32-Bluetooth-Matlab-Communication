void print_hex(const uint8_t * src_ptr, size_t src_len)
{
    size_t              i;


    for (i = 0; i < src_len; i++)
    {
        printf("%02X ", *src_ptr++);
    }
}

void test_encode_1(void)
{
    uint8_t             out_buffer[1000];
    cobs_encode_result  result;
    size_t              i;


    for (i = 0; i < dimof(test_strings); i++)
    {
        memset(out_buffer, 'A', sizeof(out_buffer));

        result = cobs_encode(out_buffer, sizeof(out_buffer), test_strings[i].data_ptr, test_strings[i].data_len);

        printf("Encoded data:\n");
        print_hex(out_buffer, result.out_len);
        printf("\nLength %zu, Status %02X\n", result.out_len, result.status);
    }
}

